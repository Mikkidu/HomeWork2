#Год рождения Пушкина 1799.06.06

rightYear = '1799'
rightDate = '06.06'

while input("Введите год рождения Пушкина: ") != rightYear:
    print("Неверный год, пропробуйте ещё раз")
while input("Введите день рождения Пушкина в формате MM.DD: ") != rightDate:
    print("Неверная дата, пропробуйте ещё раз")
print("Верно!")

